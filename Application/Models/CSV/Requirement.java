package Application.Models.CSV;

public class Requirement {

    private int id;
    private String subject;
    private int hours;
    private int minimumExperience;

    public int getId() {
        return id;
    }

    public String getSubject() {
        return subject;
    }

    public int getHours() {
        return hours;
    }

    public int getMinimumExperience() {
        return minimumExperience;
    }
}
